public class Telefono {
    protected String os;
    protected String model;
    protected String color;
    protected String ram;
    
    public Telefono() {
    }

    public Telefono(String os, String model, String color, String ram) {
        this.os = os;
        this.model = model;
        this.color = color;
        this.ram = ram;
    }

}
